package Study20210306;

public class StudentDemo02 {
    private String name;
    private int age;

    public StudentDemo02() {
    }

    public StudentDemo02(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
